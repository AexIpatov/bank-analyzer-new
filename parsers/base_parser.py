    def _read_file(self, file_content, file_name):
        """Универсальное чтение файла (Excel или CSV)"""
        with tempfile.NamedTemporaryFile(delete=False, suffix=os.path.splitext(file_name)[1]) as tmp:
            tmp.write(file_content)
            tmp_path = tmp.name
        
        try:
            if file_name.lower().endswith(('.xls', '.xlsx')):
                try:
                    df = pd.read_excel(tmp_path, engine='xlrd')
                except:
                    df = pd.read_excel(tmp_path, engine='openpyxl')
            else:
                with open(tmp_path, 'rb') as f:
                    raw = f.read()
                result = chardet.detect(raw[:10000])
                encoding = result['encoding'] if result['encoding'] else 'utf-8'
                
                for sep in [';', ',', '\t']:
                    try:
                        df = pd.read_csv(tmp_path, sep=sep, encoding=encoding, on_bad_lines='skip')
                        if len(df.columns) > 1:
                            break
                    except:
                        continue
                if len(df.columns) <= 1:
                    df = pd.read_csv(tmp_path, sep=';', encoding='latin1', on_bad_lines='skip')
        except Exception as e:
            os.unlink(tmp_path)
            raise e
        
        os.unlink(tmp_path)
        return df
    
    def _parse_date(self, date_str):
        """Преобразование даты в формат YYYY-MM-DD"""
        if pd.isna(date_str):
            return ''
        
        date_str = str(date_str)
        
        # Формат DD.MM.YYYY
        if '.' in date_str:
            parts = date_str.split('.')
            if len(parts) == 3:
                try:
                    return f"{parts[2]}-{parts[1]}-{parts[0]}"
                except:
                    pass
        
        # Формат DD.MM.YYYY HH:MM:SS
        if ' ' in date_str:
            date_str = date_str.split(' ')[0]
        
        # Формат YYYY-MM-DD
        if '-' in date_str and len(date_str) >= 10:
            return date_str[:10]
        
        return date_str[:10] if len(date_str) >= 10 else date_str
    
